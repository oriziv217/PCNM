        private int typStrToInt(String typeStr) {
        if (typeStr.equals("All Types"))
            return 0;
        else
            for (int i = 0 ; i < types.size() ; i ++)
                if (typeStr.equals(types.get(i)[1]))
                    return i+1;
        return -1;
    }
